function day1($input) {
  $lines = explode("\n", $input);
  $len = strlen($lines[0]);
  $pos = 0;
  $res = 0;
  for($i = 0; $i < sizeof($lines)-1; $i++) {
    if ($pos+3 > $len-1) {
      $next = $lines[$i+1][$pos+3-$len];
      $pos = $pos+3-$len;
    } else {
      $next = $lines[$i+1][$pos+3];
      $pos += 3;
    }
    if ($next == '#') {
      $res++;
    }
  }

  return $res;
}

function day2($input) {
  $lines = explode("\n", $input);
  $len = strlen($lines[0]);
  $res = 1;
  $steps = [
    [1 => 1],
    [3 => 1],
    [5 => 1],
    [7 => 1],
    [1 => 2],
  ];
  for($j = 0; $j < 5; $j++) {
    $trees = 0;
    $pos = 0;
    $step_r = array_keys($steps[$j])[0];
    $step_d = array_values($steps[$j])[0];
    for($i = 0; $i < sizeof($lines)-1; $i += $step_d) {
      if ($pos + $step_r > $len - $step_d) {
        $next = $lines[$i+$step_d][$pos+$step_r-$len];
        $pos = $pos+$step_r-$len;
      } else {
        $next = $lines[$i+$step_d][$pos+$step_r];
        $pos += $step_r;
      }
      if ($next == '#') {
        $trees++;
      }
    }
    $res *= $trees;
  }

  return $res;
}

